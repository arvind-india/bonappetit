package com.github.clboettcher.bonappetit.server.menu.api.dto;

import com.fasterxml.jackson.annotation.JsonSubTypes;
import com.fasterxml.jackson.annotation.JsonTypeInfo;
import io.swagger.annotations.ApiModelProperty;
import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.NoArgsConstructor;

/**
 * Abstract base class for menu item options.
 */
@Data
@AllArgsConstructor
@NoArgsConstructor
@JsonTypeInfo(
        use = JsonTypeInfo.Id.NAME,
        include = JsonTypeInfo.As.PROPERTY,
        property = "type")
@JsonSubTypes({
        @JsonSubTypes.Type(value = RadioOptionDto.class, name = "radioOption"),
        @JsonSubTypes.Type(value = CheckboxOptionDto.class, name = "checkboxOption"),
        @JsonSubTypes.Type(value = ValueOptionDto.class, name = "valueOption")})
public abstract class OptionDto {

    @ApiModelProperty(value = "The technical ID",
            required = true,
            example = "1337",
            allowableValues = "[1, infinity]")
    private Long id;

    @ApiModelProperty(value = "The title / name of this option",
            required = true,
            example = "Size")
    private String title;

    @ApiModelProperty(value = "The zero based index that this option should be displayed at in a " +
            "list of options.",
            required = true,
            example = "1",
            allowableValues = "[0, infinity]")
    private Integer index;
}
